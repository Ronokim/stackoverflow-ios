#import "UIColor+CustomColors.h"

@implementation UIColor (CustomColors)

+ (UIColor *) customLightGray{
   
    return [UIColor colorWithRed:0.88 green:0.89 blue:0.90 alpha:1.0];
}

+ (UIColor *) customGreen{
    
    return [UIColor colorWithRed:0.20 green:0.58 blue:0.26 alpha:1.0];
}

@end
